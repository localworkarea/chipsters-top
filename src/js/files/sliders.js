// Ініціалізація слайдерів
function initSliders() {
	if (document.querySelector('.swiper')) { 
		new Swiper('.swiper', { 
			// modules: [Navigation],
			observer: true,
			observeParents: true,
			slidesPerView: 3,
			spaceBetween: 20,
			speed: 500,

			// Брейкпоінти
			breakpoints: {
				320: {
					slidesPerView: 1,
					spaceBetween: 0,
					centeredSlides: true, // Центрирование активного слайда
        	initialSlide: 1,
				},
				821: {
					slidesPerView: 3,
					spaceBetween: 50,
				},
			},
			// Події
			on: {

			}
		});
	}
}
